function update(id) {
    $("#modalTitle").html('Edit meal');
    $.get(context.ajaxUrl + id, function (data) {
        $.each(data, function (key, value) {
            form.find("input[name='" + key + "']").val(value);
        });
    });
    $("#editRow").modal();
}

